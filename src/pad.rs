use crate::CrossCorrelateError;
use crate::error::try_vec;
use num_complex::Complex;

pub(crate) fn pad_real_to_complex<V: Default + Clone + Copy>(
    src: &[V],
    new_length: usize,
) -> Result<Vec<Complex<V>>, CrossCorrelateError> {
    assert!(
        new_length >= src.len(),
        "Something unexpected happened, src length must be always smaller or equal than pad length"
    );
    let mut new_vec = try_vec![Complex::<V>::default(); new_length];
    for (dst, &src) in new_vec.iter_mut().zip(src.iter()) {
        *dst = Complex {
            re: src,
            im: V::default(),
        }
    }
    Ok(new_vec)
}
